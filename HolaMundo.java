import java.util.Scanner;

public class HolaMundo {
    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);
        int repetir = scan.nextInt();

        for (int i = 0; i < repetir; i++){
            System.out.println("Hola mundo.");
        }
    }
}
